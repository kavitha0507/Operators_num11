package Operators;

public class Last {

        public static void main(String[] args) {
            // Declare two integer variables
            int x = 5;
            int y = 8;

            // Use the prefix increment operator and calculate the sum
            int sum = ++x + y;

            // Print the result
            System.out.println("Sum with prefix increment (++x): " + sum);
        }
    }



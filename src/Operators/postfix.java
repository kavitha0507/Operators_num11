package Operators;

public class postfix {

        public static void main(String[] args) {
            // Declare an integer variable and assign a number to it
            int num = 10;

            // Print the value before the increment operator
            System.out.println("Value before increment: " + num);

            // Use the postfix increment operator to increase the value
            num++;

            // Print the value after the increment operator
            System.out.println("Value after increment: " + num);
        }
    }


